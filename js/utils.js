/** Экранирует HTML-спецсимволы */
function esc(str) {
  if (str == null) return '';
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

/** Форматирует время в HH:MM */
function formatTime(date) {
  if (!date) return '';
  const d = date instanceof Date ? date : new Date(date);
  return d.toLocaleTimeString('ru', { hour: '2-digit', minute: '2-digit' });
}

/** Показывает тост-уведомление */
let _toastTimer = null;
function toast(msg, duration = 2400) {
  const el = document.getElementById('toast');
  if (!el) return;
  el.textContent = msg;
  el.classList.add('show');
  clearTimeout(_toastTimer);
  _toastTimer = setTimeout(() => el.classList.remove('show'), duration);
}

/** Haptic feedback через Telegram SDK */
function haptic(type = 'light') {
  const fb = window.Telegram?.WebApp?.HapticFeedback;
  if (!fb) return;
  if (type === 'success' || type === 'error' || type === 'warning') {
    fb.notificationOccurred(type);
  } else {
    fb.impactOccurred(type); // light | medium | heavy | rigid | soft
  }
}

/** Debounce */
function debounce(fn, ms) {
  let t;
  return function(...args) {
    clearTimeout(t);
    t = setTimeout(() => fn.apply(this, args), ms);
  };
}

/** Диалог подтверждения через TG popup или confirm */
function confirmAction(message, onConfirm) {
  const tg = window.Telegram?.WebApp;
  if (tg?.showPopup) {
    tg.showPopup(
      { message, buttons: [{ id: 'ok', type: 'destructive', text: 'Да' }, { type: 'cancel' }] },
      (id) => { if (id === 'ok') onConfirm(); }
    );
  } else {
    if (confirm(message)) onConfirm();
  }
}

/** Получить инициалы из имени */
function initials(name) {
  if (!name) return '?';
  return name.split(' ').map(s => s[0]).filter(Boolean).slice(0, 2).join('').toUpperCase();
}

